  export function getProducts() {
    const stored = localStorage.getItem('nessind_products');
    if (stored) {
      return JSON.parse(stored);
    }
    localStorage.setItem('nessind_products', JSON.stringify(products));
    return products;
  }
  
  export function saveProducts(products) {
    localStorage.setItem('nessind_products', JSON.stringify(products));
  }
  
  export function filterProducts(category, priceRange, sortBy) {
    let filtered = getProducts();
  
    if (category !== 'all') {
      filtered = filtered.filter(p => p.category === category);
    }
  
    if (priceRange !== 'all') {
      const [min, max] = priceRange.split('-').map(p => p.replace('+', ''));
      filtered = filtered.filter(p => {
        if (max) {
          return p.price >= parseInt(min) && p.price <= parseInt(max);
        } else {
          return p.price >= parseInt(min);
        }
      });
    }
  
    if (sortBy === 'price-asc') {
      filtered.sort((a, b) => a.price - b.price);
    } else if (sortBy === 'price-desc') {
      filtered.sort((a, b) => b.price - a.price);
    } else if (sortBy === 'name') {
      filtered.sort((a, b) => a.name.localeCompare(b.name));
    }
  
    return filtered;
  }
  
  export function getProductById(id) {
    return getProducts().find(p => p.id === parseInt(id));
  }
  
  export function addProduct(product) {
    const products = getProducts();
    const newId = Math.max(...products.map(p => p.id)) + 1;
    const newProduct = { ...product, id: newId };
    products.push(newProduct);
    saveProducts(products);
    return newProduct;
  }
  
  export function updateProduct(id, updatedProduct) {
    const products = getProducts();
    const index = products.findIndex(p => p.id === parseInt(id));
    if (index !== -1) {
      products[index] = { ...products[index], ...updatedProduct };
      saveProducts(products);
      return products[index];
    }
    return null;
  }
  
  export function deleteProduct(id) {
    const products = getProducts();
    const filtered = products.filter(p => p.id !== parseInt(id));
    saveProducts(filtered);
  }
  